//Operadores de comparación
let b = 3, c = 4, d = '3';
//Comparador simple de igualdad (solo compara valores)
let z = b == d;
console.log(z);
//Comparador estricto de igualdad (compara valores y tipos de dato)
z = b === d;
console.log(z);
//Comparador simple de diferencia (solo compara valores)
z = b != d;
console.log(z);
//Comparador estricto de diferencia (compara valores y tipos de dato)
z = b !== d;
console.log(z);

